package com.songdroid.spring.domain.board;

public class MasterBoardDto {
	private int board_num;
	private String board_disp_name;
	private String board_tab_name;
	private String board_create_date;
	private String board_reply;
	private String board_comment;
	private String board_upload;
	
	public int getBoard_num() {
		return board_num;
	}
	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
	public String getBoard_disp_name() {
		return board_disp_name;
	}
	public void setBoard_disp_name(String board_disp_name) {
		this.board_disp_name = board_disp_name;
	}
	public String getBoard_tab_name() {
		return board_tab_name;
	}
	public void setBoard_tab_name(String board_tab_name) {
		this.board_tab_name = board_tab_name;
	}
	public String getBoard_create_date() {
		return board_create_date;
	}
	public void setBoard_create_date(String board_create_date) {
		this.board_create_date = board_create_date;
	}
	public String getBoard_reply() {
		return board_reply;
	}
	public void setBoard_reply(String board_reply) {
		this.board_reply = board_reply;
	}
	public String getBoard_comment() {
		return board_comment;
	}
	public void setBoard_comment(String board_comment) {
		this.board_comment = board_comment;
	}
	public String getBoard_upload() {
		return board_upload;
	}
	public void setBoard_upload(String board_upload) {
		this.board_upload = board_upload;
	}
}
